package com.genokiller.Task1;

/**
 * Інтерфейс {@code IGraphic} визначає базову структуру для всіх графічних об'єктів.
 * <p>
 * Кожен графічний елемент має координати (x, y), розміри (width, height)
 * і може виводити інформацію про себе.
 * <p>
 * Використовується у патерні <b>Composite</b> для роботи як із примітивами,
 * так і з групами об’єктів через єдиний інтерфейс.
 */
public interface IGraphic {

    /**
     * Повертає координату X графічного об'єкта.
     *
     * @return поточна координата X
     */
    double getX();

    /**
     * Повертає координату Y графічного об'єкта.
     *
     * @return поточна координата Y
     */
    double getY();

    /**
     * Повертає ширину графічного об'єкта.
     *
     * @return поточна ширина об'єкта
     */
    double getWidth();

    /**
     * Повертає висоту графічного об'єкта.
     *
     * @return поточна висота об'єкта
     */
    double getHeight();

    /**
     * Виводить інформацію про графічний об'єкт у консоль.
     * <p>
     * Використовується для демонстрації властивостей (координат, розмірів тощо).
     */
    void showInfo();
}
